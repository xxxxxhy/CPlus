#include<iostream>
using namespace std;

int main(){
	
	// 比较运算符 用于表达式的比较，并返回一个逻辑值
	// == 相等于 != 不等于 < 小于 > 大于 <= 小于等于 >= 大于等于
	int a = 10;
	int b = 20;
	
	cout << (a==b) << endl;
	cout << (a!=b) << endl;
	cout << (a>b) << endl;
	cout << (a<b) << endl;
	cout << (a<=b) << endl;
	cout << (a>=b) << endl;
	

	system("ls");

	return 0;

}
